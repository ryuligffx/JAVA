//loops are almost as same as C
public class Loops {
    public static void main(String[] args){
        int i;
        for(i=1;i<=10;i++){
            System.out.println(i);
        }

        int j=1;
        while(j<=10){
            System.out.println(j);
            j++;
        }

        int k = 0;
        do{
            System.out.println(k);
            k++;
        }while(k<10);
    }
}
